static void fb_clear(uint16_t color) {
    for (int i = 0; i < LCD_WIDTH * LCD_HEIGHT; i++) framebuf[i] = color;
}

static void fb_draw_char(int x, int y, char c, uint16_t fg, uint16_t bg, int scale) {
    if (c < 32 || c > 126) c = ' ';
    const uint8_t *glyph = font8x16[c - 32];
    
    for (int row = 0; row < 16; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (0x80 >> col)) {
                for (int sy = 0; sy < scale; sy++) {
                    for (int sx = 0; sx < scale; sx++) {
                        int px = x + col * scale + sx;
                        int py = y + row * scale + sy;
                        if (px < LCD_WIDTH && py < LCD_HEIGHT) {
                            framebuf[py * LCD_WIDTH + px] = fg;
                        }
                    }
                }
            }
        }
    }
}

static void fb_draw_text(int x, int y, const char *str, uint16_t fg, uint16_t bg, int scale) {
    while (*str) {
        fb_draw_char(x, y, *str, fg, bg, scale);
        x += 8 * scale;
        str++;
    }
}

static void fb_flush(void) {
    esp_lcd_panel_draw_bitmap(panel_handle, 0, 0, LCD_WIDTH, LCD_HEIGHT, framebuf);
}

static void init_display(void) {
    spi_bus_config_t buscfg = {
        .mosi_io_num = PIN_NUM_MOSI, .miso_io_num = -1, .sclk_io_num = PIN_NUM_SCLK,
        .max_transfer_sz = LCD_WIDTH * LCD_HEIGHT * 2,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(LCD_HOST, &buscfg, SPI_DMA_CH_AUTO));

    esp_lcd_panel_io_spi_config_t io_config = {
        .cs_gpio_num = PIN_NUM_CS, .dc_gpio_num = PIN_NUM_DC,
        .spi_mode = 0, .pclk_hz = 10 * 1000 * 1000,
        .trans_queue_depth = 10, .lcd_cmd_bits = 8, .lcd_param_bits = 8,
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_io_spi(LCD_HOST, &io_config, &io_handle));

    esp_lcd_panel_dev_config_t panel_config = {
        .reset_gpio_num = PIN_NUM_RST, .color_space = ESP_LCD_COLOR_SPACE_RGB, .bits_per_pixel = 16,
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_st7735(io_handle, &panel_config, &panel_handle));

    esp_lcd_panel_reset(panel_handle);
    esp_lcd_panel_init(panel_handle);
    
    
    esp_lcd_panel_invert_color(panel_handle, false); 
    esp_lcd_panel_swap_xy(panel_handle, true);
    esp_lcd_panel_mirror(panel_handle, true, false); 
    esp_lcd_panel_set_gap(panel_handle, 0, 24);

    esp_lcd_panel_disp_on_off(panel_handle, true);
    fb_clear(0x0000); 
    fb_flush();
}

static void sensor_task(void *pvParameters) {
    uint16_t co2; float temp, hum; char buf[32];
    while (1) {
        if (xSemaphoreTake(i2c_mutex, pdMS_TO_TICKS(1000)) == pdTRUE) {
            if (scd41_data_ready_check(I2C_PORT) && scd41_read_measurement(I2C_PORT, &co2, &temp, &hum) == ESP_OK) {
                fb_clear(0x0000);

                
                fb_draw_text(5, 5, "CO2:", 0xFFFF, 0x0000, 1);
                snprintf(buf, sizeof(buf), "%d", co2);
                fb_draw_text(60, 5, buf, 0x07FF, 0x0000, 2); 

                fb_draw_text(5, 32, "TEMP:", 0xFFFF, 0x0000, 1);
                snprintf(buf, sizeof(buf), "%.1f", temp);
                fb_draw_text(60, 32, buf, 0x07FF, 0x0000, 2);

                fb_draw_text(5, 58, "HUM:", 0xFFFF, 0x0000, 1);
                snprintf(buf, sizeof(buf), "%.1f%%", hum);
                fb_draw_text(60, 58, buf, 0x07FF, 0x0000, 2);

                fb_flush();
            }
            xSemaphoreGive(i2c_mutex);
        }
        vTaskDelay(pdMS_TO_TICKS(2000));
    }
}

void app_main(void) {
    nvs_flash_init();
    i2c_mutex = xSemaphoreCreateMutex();
    scd41_i2c_init(I2C_PORT, I2C_SDA_PIN, I2C_SCL_PIN);
    init_display();
    scd41_start_periodic(I2C_PORT);
    xTaskCreate(sensor_task, "sensor_task", 4096, NULL, 5, NULL);
}